#include "pong.h"

void print_campo(pong *var)
{
    int i = 0;
    int j;
    while (i < V)
    {
        j = 0;
        while (j < H)
        {
            printf("%c", var->campo[i][j]);
            j++;
        }
        printf("\n");
        i++;
    }
}